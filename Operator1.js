/*
+ =>sum
- =>subraction
* =>multiplication
/ =>division
% =>remainder
*/

var num1=100
var num2=200
console.log(num1+num2)
console.log(num1-num2)
console.log(num1*num2)
console.log(num1/num2)
console.log(num1%num2) //%-Modulo for remainder return 100%200=Remainder>return 100 as Numerator is greater than denominator


